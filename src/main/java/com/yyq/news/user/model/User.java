package com.yyq.news.user.model;

public class User {

	private Integer u_id;
	
	private String u_name;
	
	private Integer u_sex;
	
	private Integer u_age;
	
	private String account;
	
	private String password;
	
	private String u_address;
	
	private String u_tel;
	
	private String u_email;
	
	private Boolean u_dr;

	public Integer getU_id() {
		return u_id;
	}

	public void setU_id(Integer u_id) {
		this.u_id = u_id;
	}

	public String getU_name() {
		return u_name;
	}

	public void setU_name(String u_name) {
		this.u_name = u_name;
	}

	public Integer getU_sex() {
		return u_sex;
	}

	public void setU_sex(Integer u_sex) {
		this.u_sex = u_sex;
	}

	public Integer getU_age() {
		return u_age;
	}

	public void setU_age(Integer u_age) {
		this.u_age = u_age;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getU_address() {
		return u_address;
	}

	public void setU_address(String u_address) {
		this.u_address = u_address;
	}

	public String getU_tel() {
		return u_tel;
	}

	public void setU_tel(String u_tel) {
		this.u_tel = u_tel;
	}

	public String getU_email() {
		return u_email;
	}

	public void setU_email(String u_email) {
		this.u_email = u_email;
	}

	public Boolean getU_dr() {
		return u_dr;
	}

	public void setU_dr(Boolean u_dr) {
		this.u_dr = u_dr;
	}
	
}
